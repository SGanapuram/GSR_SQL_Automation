create trigger [dbo].[market_pricing_cond_updtrg]
on [dbo].[market_pricing_condition]
for update
as
declare @num_rows         int,
        @count_num_rows   int,
        @dummy_update     int,
        @errmsg           varchar(255)

select @num_rows = @@rowcount
if @num_rows = 0
   return

select @dummy_update = 0

/* RECORD_STAMP_BEGIN */
if not update(trans_id) 
begin
   raiserror ('(market_pricing_condition) The change needs to be attached with a new trans_id',10,1)
   if @@trancount > 0 rollback tran

   return
end

/* added by Peter Lo  Sep-4-2002 */
if exists (select 1
           from master.dbo.sysprocesses
           where spid = @@spid and
                (rtrim(program_name) IN ('ISQL-32', 'OSQL-32', 'SQL Query Analyzer', 'SQLCMD') OR
                 program_name like 'Microsoft SQL Server Management Studio%') )
begin
   if (select count(*) from inserted, deleted where inserted.trans_id <= deleted.trans_id) > 0
   begin
      select @errmsg = '(market_pricing_condition) New trans_id must be larger than original trans_id.'
      select @errmsg = @errmsg + char(10) + 'You can use the the gen_new_transaction procedure to obtain a new trans_id.'
      raiserror (@errmsg,10,1)
      if @@trancount > 0 rollback tran

      return
   end
end

if exists (select * from inserted i, deleted d
           where i.trans_id < d.trans_id and
                 i.cmf_num = d.cmf_num and
                 i.mkt_pricing_cond_num = d.mkt_pricing_cond_num)
begin
   raiserror ('(market_pricing_condition) new trans_id must not be older than current trans_id.',10,1)
   if @@trancount > 0 rollback tran

   return
end

/* RECORD_STAMP_END */

if  update(cmf_num) or
    update(mkt_pricing_cond_num)
begin
      select @count_num_rows = (select count(*) from inserted i, deleted d
                                where i.cmf_num = d.cmf_num and
                                      i.mkt_pricing_cond_num = d.mkt_pricing_cond_num)
   if (@count_num_rows = @num_rows)
   begin
      select @dummy_update = 1
   end
   else
   begin
      raiserror ('(market_pricing_condition) primary key can not be changed.',10,1)
      if @@trancount > 0 rollback tran

      return
   end
end

/* AUDIT_CODE_BEGIN */

if @dummy_update = 0
   insert dbo.aud_market_pricing_condition
      (cmf_num,
       mkt_pricing_cond_num,
       mkt_cond_type,
       mkt_cond_date,
       mkt_cond_quote_range,
       mkt_cond_last_next_ind,
       trans_id,
       resp_trans_id)
