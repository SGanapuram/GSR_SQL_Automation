      d.cmf_num,
      d.mkt_pricing_cond_num,
      d.mkt_cond_type,
      d.mkt_cond_date,
      d.mkt_cond_quote_range,
      d.mkt_cond_last_next_ind,
      d.trans_id,
      i.trans_id
   from deleted d, inserted i
   where d.cmf_num = i.cmf_num and
         d.mkt_pricing_cond_num = i.mkt_pricing_cond_num 

