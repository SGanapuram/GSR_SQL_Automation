create trigger [dbo].[trading_period_deltrg]
on [dbo].[trading_period]
for delete
as
declare @num_rows    int,
        @errmsg      varchar(255),
        @atrans_id   int,
        @tablename   varchar(30)

select @num_rows = @@rowcount
if @num_rows = 0
   return

/* AUDIT_CODE_BEGIN */
select @atrans_id = max(trans_id)
from dbo.icts_transaction WITH (INDEX=icts_transaction_idx4)
where spid = @@spid and
      tran_date >= (select top 1 login_time
                    from master.dbo.sysprocesses (nolock)
                    where spid = @@spid)

if @atrans_id is null
begin
   select @errmsg = '(trading_period) Failed to obtain a valid responsible trans_id.'
   if exists (select 1
              from master.dbo.sysprocesses (nolock)
              where spid = @@spid and
                    (rtrim(program_name) IN ('ISQL-32', 'OSQL-32', 'SQL Query Analyzer', 'SQLCMD') OR
                     program_name like 'Microsoft SQL Server Management Studio%') )
      select @errmsg = @errmsg + char(10) + 'You must use the gen_new_transaction procedure to obtain a new trans_id before executing delete statement.'
   raiserror (@errmsg,10,1)
   if @@trancount > 0 rollback tran

   return
end

insert dbo.aud_trading_period
   (commkt_key,
    trading_prd,
    last_trade_date,
    opt_exp_date,
    first_del_date,
    last_del_date,
    first_issue_date,
    last_issue_date,
    last_quote_date,
    trading_prd_desc,
    opt_eval_method,
    trans_id,
    resp_trans_id)
select
   d.commkt_key,
   d.trading_prd,
   d.last_trade_date,
   d.opt_exp_date,
   d.first_del_date,
   d.last_del_date,
   d.first_issue_date,
